package modelo;

public class Gerente {

    int idGerente;
    String nomeGerente;
    String emailGerente;
    String enderecoGerente;
    int cpfGerente;
    String telefoneGerente;
    String cepGerente;
    String generoGerente;
    int idFuncionario;

    public int getIdGerente() {
        return idGerente;
    }

    public void setIdGerente(int idGerente) {
        this.idGerente = idGerente;
    }

    public String getNomeGerente() {
        return nomeGerente;
    }

    public void setNomeGerente(String nomeGerente) {
        this.nomeGerente = nomeGerente;
    }

    public String getEmailGerente() {
        return emailGerente;
    }

    public void setEmailGerente(String emailGerente) {
        this.emailGerente = emailGerente;
    }

    public String getEnderecoGerente() {
        return enderecoGerente;
    }

    public void setEnderecoGerente(String enderecoGerente) {
        this.enderecoGerente = enderecoGerente;
    }

    public int getCpfGerente() {
        return cpfGerente;
    }

    public void setCpfGerente(int cpfGerente) {
        this.cpfGerente = cpfGerente;
    }

    public String getTelefoneGerente() {
        return telefoneGerente;
    }

    public void setTelefoneGerente(String telefoneGerente) {
        this.telefoneGerente = telefoneGerente;
    }

    public String getCepGerente() {
        return cepGerente;
    }

    public void setCepGerente(String cepGerente) {
        this.cepGerente = cepGerente;
    }

    public String getGeneroGerente() {
        return generoGerente;
    }

    public void setGeneroGerente(String generoGerente) {
        this.generoGerente = generoGerente;
    }

    public int getIdFuncionario() {
        return idFuncionario;
    }

    public void setIdFuncionario(int idFuncionario) {
        this.idFuncionario = idFuncionario;
    }

}
